package loads

import (
	"jvm/instructions/base"
	"jvm/rtda"
	"jvm/rtda/heap"
)

//加载引用类型数组
type AALOAD struct{
	base.NoOperandsInstruction
}

func (self *AALOAD) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	index := stack.PopInt()
	arrRef := stack.PopRef()

	checkNotNil(arrRef)
	refs := arrRef.Refs()
	checkIndex(len(refs), index)
	stack.PushRef(refs[index])
}

